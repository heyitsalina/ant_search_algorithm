class Colony:
    def __init__(self, amount, size, coordinates, color):
        """
        This class represents the Ant-colony 

        Args:
        amount (float):
            amount of Ants per colony
        size (float):
            size of the colonys nest?
        coordinates (tuple):
            The (x,y) coordinates of the ants nest?
        """

        self.amount = amount
        self.size = size
        self.coordinates = coordinates
        self.color = color

    
    
